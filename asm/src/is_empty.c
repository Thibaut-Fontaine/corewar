# include "...include/ft_asm.h"

int			is_empty(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (!ft_iswhitespace(str[i]) && str[i] != '\n')
			return(0);
		i++;
	}
	return (1);
}
